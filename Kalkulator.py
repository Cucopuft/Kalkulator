# Membuat Kalkulator Sederhana

# Fungsi ini digunakan untuk penambahan dua angka
def add(x, y):
    return x + y

# Fungsi ini digunakan untuk mengurangi dua angka
def subtract(x, y):
    return x - y

# Fungsi ini mengalikan dua angka
def multiply(x, y):
    return x * y

# Fungsi ini digunakan untuk pembagian 
def divide(x, y):
    return x / y